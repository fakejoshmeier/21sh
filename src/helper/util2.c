#include "sh.h"

void	setshell(t_sh *sh)
{
	char	*arr[3];

	arr[0] = "setenv";
	arr[1] = "SHELL";
	arr[2] = "21sh";
	b_setenv(3, arr, sh);
	sh->history = NULL;
	sh->curr = NULL;
}